//OPERADORES RELACIONALES
let a = 10;
//cuando usemos operadores de comparación,
//nos va a devolver un boolean
console.log(a > 5);

let b = 5;
console.log(b > 5);
console.log(b >= 5);
console.log(b < 5);
console.log(b <= 5);


//OPERADORES DE IGUALDAD
let c = 10;
console.log(c == 10);
console.log(c != 10);
console.log(c == "10"); //devuelve true porque estamos preguntando
                        //por la igualdad del valor
console.log(c ==="10"); //devuelve false porque estamos preguntando 
                        //por la igualdad del valor y del tipo del dato
console.log(c !== "10");


